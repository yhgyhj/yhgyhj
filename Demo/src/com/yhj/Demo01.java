public class Demo01 {
	 public static void main(String[] args) {
	        // 定义一个int类型的变量a1
	        int a1;
	        // 给变量a赋值
	        a1=1;
	        System.out.println("a1="+a1);
	         
	        // 定义一个int类型的变量a2 并且赋值
	        int a2=2;
	        System.out.println("a2="+a2);
	         
	        // 定义一个byte类型的变量b并且赋值
	        byte b=3;
	        System.out.println("b="+b);
	         
	        // 定义一个short类型的变量s
	        short s=4;
	        System.out.println("s="+s);
	         
	        // 定义一个long类型的变量l
	        long l=5;
	        System.out.println("l="+l);
	         
	        int a11=1;
	        int a22=2;
	        // 简单的一个表达式计算
	        int a3=a11+a22;
	        System.out.println("a11+a22="+a3);
	        // 定义一个float类型变量f
	        // 小数默认是double类型，所以我们必须加一个f，来表示float类型
	        float f=0.1f;
	        System.out.println("f="+f);
	         
	        // 定义一个double类型变量d
	        double d=1.2;
	        // 快捷方式 syso alt+/
	        System.out.println("d="+d);
	        //float的最小值
	        float mix=Float.MAX_EXPONENT;
	        //float的最大值
	        float maxF=Float.MAX_VALUE;
	        System.out.println("float的最大值："+maxF);
	        System.out.println("float的最小值："+mix);
	        //double的最小值
	        double mix1=Double.MAX_EXPONENT;
	        //double的最大值
	        double max=Double.MAX_EXPONENT;
	        System.out.println("double的最小值："+mix1);
	        System.out.println("double的最大值："+max);
	        //输出1+2+3+4+5的总和
	        int a=1+2+3+4+5;
	        System.out.println("1+2+3+4+5="+a);
	        //布尔类型
	        boolean b1=true;
	        System.out.println("b1="+b1);
	         
	        // 定义一个布尔类型的变量b2
	        boolean b2=false;
	        System.out.println("b2="+b2);
	    }
}
